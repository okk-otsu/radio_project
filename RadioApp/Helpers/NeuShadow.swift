import SwiftUI

/// Модификатор для создания неоморфного (neumorphic) теневого эффекта
/// Неоморфизм - современный стиль дизайна, имитирующий физические объекты с мягкими тенями
struct NeuShadow: ViewModifier {
    
    /// Основная функция модификатора, применяющая тени к контенту
    /// - Parameter content: Представление, к которому применяются тени
    /// - Returns: Модифицированное представление с неоморфными тенями
    func body(content: Content) -> some View {
        content
            // MARK: - Первая тень (светлая/верхняя)
            /// Создает светлую тень в левом верхнем углу для эффекта "выступающей" поверхности
            /// - color: Цвет светлой тени (обычно светлее фона)
            /// - radius: Минимальный радиус размытия для четкого края
            /// - x: Горизонтальное смещение (-7 = влево)
            /// - y: Вертикальное смещение (-7 = вверх)
            /// Назначение: Имитирует источник света сверху слева
            .shadow(color: Color("neuColor1"), radius: 1, x: -7, y: -7)
            
            // MARK: - Вторая тень (светлая/усиленная)
            /// Усиливает светлый эффект с большим смещением и размытием
            /// - radius: Увеличенный радиус для более мягкого края
            /// - x: Большее смещение влево (-13)
            /// - y: Смещение вверх (-7)
            /// Назначение: Создает более выраженный блик
            .shadow(color: Color("neuColor2"), radius: 4, x: -13, y: -7)
            
            // MARK: - Третья тень (темная/нижняя)
            /// Создает темную тень в правом нижнем углу для глубины
            /// - radius: Средний радиус размытия
            /// - x: Небольшое смещение влево (-8)
            /// - y: Смещение вниз (5)
            /// Назначение: Имитирует тень от выступающего объекта
            .shadow(color: Color("neuColor3"), radius: 10, x: -8, y: 5)
            
            // MARK: - Четвертая тень (темная/контрастная)
            /// Добавляет контрастную темную тень с противоположным смещением
            /// - radius: Такой же радиус как у третьей тени для баланса
            /// - x: Смещение вправо (10)
            /// - y: Смещение вниз (9)
            /// Назначение: Создает объем и глубину, усиливая 3D-эффект
            .shadow(color: Color("neuColor4"), radius: 10, x: 10, y: 9)
    }
}

// MARK: - Расширение для удобного использования
extension View {
    /// Упрощенный доступ к неоморфному теневому эффекту
    /// - Returns: Представление с примененным неоморфным эффектом
    ///
    /// Пример использования:
    /// ```
    /// Rectangle()
    ///     .fill(Color.gray)
    ///     .frame(width: 100, height: 100)
    ///     .neuShadow()
    /// ```
    func neuShadow() -> some View {
        self.modifier(NeuShadow())
    }
}

/*
 ПРИМЕЧАНИЯ ПО ИСПОЛЬЗОВАНИЮ:
 
 1. ЦВЕТА ДЛЯ НЕОМОРФНОГО ЭФФЕКТА:
    - neuColor1: Очень светлый оттенок (блик)
    - neuColor2: Светлый оттенок с небольшим контрастом
    - neuColor3: Темный оттенок для основной тени
    - neuColor4: Более темный оттенок для контрастной тени
 
 2. РЕКОМЕНДАЦИИ:
    - Используйте на элементах с закругленными углами
    - Работает лучше всего на фоне, близком к цвету элемента
    - Избегайте использования на очень маленьких элементах
 
 3. НАСТРОЙКА ДЛЯ РАЗНЫХ ТЕМ:
    - Для светлой темы: светлые тени почти белые, темные - серые
    - Для темной темы: светлые тени - светлые серые, темные - почти черные
