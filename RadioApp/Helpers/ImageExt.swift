import SwiftUI

// MARK: - Расширение для унифицированного управления изображениями приложения
/// Централизованное хранилище иконок приложения
/// Преимущества:
/// - Единая точка управления всеми изображениями
/// - Автодополнение и проверка типов
/// - Легкий рефакторинг
/// - Предотвращение опечаток в названиях изображений
public extension Image {
    
    // MARK: - Иконки интерфейса
    
    /// Иконка закрытия/стрелка вниз
    /// Использование: закрытие модальных окон, выпадающих меню
    /// Дизайн: стрелка, направленная вниз
    static let close = Image("arrow_down")
    
    /// Иконка настроек/опций
    /// Использование: открытие меню настроек, дополнительных опций
    /// Дизайн: три точки или шестеренка
    static let options = Image("options_icon")
    
    /// Иконка поиска
    /// Использование: поле поиска, кнопка поиска
    /// Дизайн: лупа
    static let search = Image("search_icon")
    
    // MARK: - Иконки управления медиа
    
    /// Иконка воспроизведения
    /// Использование: запуск трека, видео
    /// Дизайн: треугольник, направленный вправо
    static let play = Image("play_icon")
    
    /// Иконка паузы
    /// Использование: приостановка воспроизведения
    /// Дизайн: две вертикальные линии
    static let pause = Image("pause_icon")
    
    /// Иконка следующего трека
    /// Использование: переход к следующему треку
    /// Дизайн: две стрелки вправо или одиночная с вертикальной линией
    static let next = Image("next_icon")
    
    // MARK: - Иконки взаимодействия
    
    /// Иконка "лайк" (незаполненная)
    /// Использование: добавление в избранное, лайк
    /// Дизайн: контур сердца
    /// Состояние: неактивное/невыбранное
    static let heart = Image("heart_icon")
    
    /// Иконка "лайк" (заполненная)
    /// Использование: отображение активного состояния избранного
    /// Дизайн: заполненное сердца
    /// Состояние: активное/выбранное
    static let heart_filled = Image("heart-filled_icon")
}

/*
 ПРИМЕЧАНИЯ ПО ИСПОЛЬЗОВАНИЮ:
 
 1. ПРЕИМУЩЕСТВА СТАТИЧЕСКИХ СВОЙСТВ:
    - Автодополнение: Xcode подсказывает доступные иконки
    - Безопасность типов: компилятор проверяет правильность использования
    - Централизованное управление: все изменения в одном месте
 
 2. КОНВЕНЦИИ ИМЕНОВАНИЯ:
    - snake_case для имен свойств
    - Суффикс _icon в названиях файлов для группировки
    - Четкое разделение состояний (heart vs heart_filled)
 
 3. ПРАКТИКИ ИСПОЛЬЗОВАНИЯ:
 
    // ✅ ПРАВИЛЬНО - типобезопасно и с автодополнением
    Button(action: play) {
        Image.play
            .resizable()
            .frame(width: 24, height: 24)
    }
 
    // ❌ НЕ РЕКОМЕНДУЕТСЯ - риск опечаток
    Button(action: play) {
        Image("play_icon")  // Возможна ошибка в названии
    }
 
 4. СОВМЕСТИМОСТЬ С РЕСУРСАМИ:
    Убедитесь, что в Assets.xcassets присутствуют все указанные изображения:
    - arrow_down
    - options_icon
    - search_icon
    - play_icon
    - pause_icon
    - heart_icon
    - heart-filled_icon
    - next_icon
 
 5. РАСШИРЕНИЕ ФУНКЦИОНАЛА:
    Для дополнительного удобства можно добавить модификаторы:
    
    extension Image {
        func iconStyle() -> some View {
            self
                .resizable()
                .aspectRatio(contentMode: .fit)
                .frame(width: 24, height: 24)
        }
    }
    
    Использование:
    Image.play.iconStyle()
